    private void clearTable(JTable table)
    {
        for(int i=table.getRowCount()-1; i>=0; i--)
        {
            ((DefaultTableModel)table.getModel()).removeRow(i);
        }
    }

    private void Fetch()
    {
        // 3 Columns
        // {"User Name","Log-in Time","Log-out Time",
        clearTable(jTable1);
        String query="select unm,login,logout from LoginDetails,UserMaster where LoginDetails.uid="
                + "UserMaster.uid order by login desc";
        System.out.println(query);
        dBConnection db=new dBConnection();
        Connection conn=db.setConnection();
        int total=0;
        try
        {
            Statement smt=conn.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs=smt.executeQuery(query);
            //Move to the last record
            rs.afterLast();
            //Get the current record position
            if(rs.previous())
                total = rs.getRow();
            //Move back to the first record;
            rs.beforeFirst();
            if(total != 0)
            {
		while(rs.next())
		{
                    Vector<String> row = new Vector<String>();
                    row.addElement(rs.getString("unm"));
                    row.addElement(rs.getString("login"));
                    row.addElement(rs.getString("logout"));
                    ((DefaultTableModel)jTable1.getModel()).addRow(row);
		}
            }
        }
        catch(SQLException ex)
        {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null,"LoginDetails ex1: "+ex.getMessage(),
                    "Error Found",JOptionPane.ERROR_MESSAGE);
            return;
        }
        finally
        {
            try {
                if (conn!=null) conn.close();
            }
            catch(SQLException e){}
        }

        jTable1.setDragEnabled(false);
        // Disable auto resizing
        jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        JTableHeader header = jTable1.getTableHeader();
        header.setBackground(Color.cyan);
        //Start resize the table column
        // 3 Columns
        // {"User Name","Log-in Time","Log-out Time",
        jTable1.getColumnModel().getColumn(0).setMinWidth(0);// User Name
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(200);
        jTable1.getColumnModel().getColumn(1).setMinWidth(0);// Log-in Time
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(200);
        jTable1.getColumnModel().getColumn(2).setMinWidth(0);// Log-out Time
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(200);
    }

